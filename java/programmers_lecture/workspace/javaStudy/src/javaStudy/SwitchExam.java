package javaStudy;

public class SwitchExam {

	public static void main(String[] args) {
		//switch, case, default, break
		
//		int value = 1;
		
		switch("value") {
			case "value":
				System.out.println("1");
//				break;
			case "a":
				System.out.println("2");
				break;
			case "c":
				System.out.println("3");
				break;
			default:
				System.out.println("another");
		}
	}

}
